export const findVietnamBankByCode = (code) => {
     const normalizedCode = (code || "").trim().toLowerCase();
     if (!normalizedCode) {
          return undefined;
     }
     return VIETNAM_BANKS.find((bank) => {
          const primaryMatch = bank.code.toLowerCase() === normalizedCode;
          const aliasMatch = (bank.aliases || []).some((alias) => alias.toLowerCase() === normalizedCode);
          return primaryMatch || aliasMatch;
     });
};

